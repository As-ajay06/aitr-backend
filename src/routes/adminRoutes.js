const express = require('express');
const adminRouter = express.Router();

// Import and define routes for admin profile
const { createPersonalInfo, getAllPersonalInfo, getPersonalInfoById, updatePersonalInfo, deletePersonalInfo } = require('../controllers/admin/adminController');
adminRouter.post('/profile', createPersonalInfo);
adminRouter.get('/profiles', getAllPersonalInfo);
adminRouter.get('/profile/:id', getPersonalInfoById);
adminRouter.put('/profile/:id', updatePersonalInfo);
adminRouter.delete('/profile/:id', deletePersonalInfo);


module.exports = adminRouter;